package ru.mrchebik.task15.pixel;

import javafx.scene.paint.Color;

/**
 * Created by mrchebik on 8/19/17.
 */
public class Pixel {
    private int x;
    private int y;
    private Color color;

    public Pixel(int x, int y, Color color) {
        this.x = x;
        this.y = y;
        this.color = color;
    }


    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public Color getColor() {
        return color;
    }

    public void setColor(Color color) {
        this.color = color;
    }
}
